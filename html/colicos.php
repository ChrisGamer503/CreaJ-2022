                        <li>Crying for no apparent reason, as opposed to crying to express hunger or the need for a
                            diaper change</li>
                        <li>Extreme nervousness, even after the crying subsided</li>
                        <li>Predictable timing, with episodes often occurring at night</li>
                        <li>Facial discoloration, such as flushing or reddening of the skin</li>
                        <li>Body tension, for example, stretching and tensing your legs, tensing your arms, clenching
