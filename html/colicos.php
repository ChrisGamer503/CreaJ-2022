                                time than other children..
                            </p>
                            <p> They can be frustrating for parents because the baby's distress occurs for no reason
                                and there seems to be no way to relieve it, Cramps usually get better by
                                on their own, and most babies "outgrow" them by six months of age.
                            </p>
