                        <li>Digestive system that has not fully developed</li>
                        <li>Imbalance of healthy bacteria in the digestive tract</li>
                        <li>Food allergies or food intolerances</li>
                        <li>Underfeeding, overeating, or infrequent burping</li>
                        <li>Early childhood migraine</li>
                        <li>Family stress or anxiety</li>
                    </ul>
                </div>
                <div class="sintoma">
                    <p>Symptom</p>
                </div>
                <div class="lista-sin">
                    <ul>
