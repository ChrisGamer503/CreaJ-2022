                                Stuffy and runny noses are the main signs of a cold. As usual
                                It's harmless, although it may not look like it.
                            </p>
                            <p> Babies are especially susceptible to the common cold, in part because they are often
                                surrounded by older children. In addition, they have not yet developed immunity to many
