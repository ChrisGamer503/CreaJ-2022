                <div class="sintom">
                    <p>Causes</p>
                </div>
                <div class="info-causa">
                    <p>
                        Although many types of viruses can cause the common cold, rhinoviruses are the most common
                        cause.
                    </p>
                    <p>
                        A cold virus enters the body through the mouth, eyes, or nose. The virus can spread through
                        droplets in the air when someone who is sick coughs, sneezes, or talks.
                    </p>
                    <p>
                        It is also spread by hand-to-hand contact with someone who has a cold or by sharing contaminated
                        objects, such as eating utensils, towels, toys, or phones. If you touch your eyes, nose, or
                        mouth after such contact, you are likely to catch a cold.
                    </p>
                </div>
                <div class="sintoma">
                    <p>Symptom</p>
                </div>
