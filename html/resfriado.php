                        <li>Wheeze while breathing</li>
                        <li>Severe throat, headache, or sinus pains</li>
                        <li>Ear pain</li>
                        <li>Extreme irritability</li>
                        <li>Unusual sleepiness</li>
                        <li>Lack of appetite</li>
                    </ul>
