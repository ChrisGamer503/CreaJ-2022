                        <li>Congestion</li>
                        <li>Sneezing</li>
                        <li>Low grade fever</li>
                        <li>Usually not feeling well</li>
                        <li>Symptoms that get worse or do not improve</li>
                        <li>Fever that returns after a period without fever</li>
