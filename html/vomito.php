                <div class="sintom">
                    <p>Causas</p>
                </div>
                <div class="info-causa">
                    <ul>
                        <li>Alergias a los alimentos.</li>
                        <li>Migrañas.</li>
                        <li>Infecciones estomacales o intestinales, como la "gastroenteritis vírica epidémica" o la
                            intoxicación alimentaria.</li>
                        <li>Medicamentos o tratamientos, como la quimioterapia o la radioterapia para el cáncer</li>
                        <li>Devolución de los contenidos estomacales (alimento o líquido) (también llamado reflujo
                            gastroesofágico o ERGE).</li>
                        <li>Náuseas matutinas durante el embarazo.</li>
                        <li>Mareo o mareo por movimiento.</li>
                        <li>Dolor intenso, como el causado por los cálculos renales.</li>
                    </ul>
                </div>
                <div class="sintoma">
                    <p>Sintomas</p>
                </div>
                <div class="lista-sin">
                    <ul>
                        <li>Falta de apetito</li>
                        <li>Deshidratación luego de vomitar.</li>
                        <li>Vertigo.</li>
                        <li> Letargo o apatía.</li>
                        <li>En los bebés, desconsuelo o irritabilidad y abultamiento de los puntos blandos entre los
                            huesos del cráneo (fontanelas).</li>
                        <li>En los niños mayores, fuerte dolor de cabeza, rigidez de nuca que hace difícil bajar la
                            barbilla hasta el pecho, sensibilidad a la luz y fiebre.</li>
                        <li>Dolor y/o hinchazón abdominal.</li>
                        <li>Vómitos persistentes en niños que no crecen o se desarrollan de la forma esperada.</li>
                        <li>Deposiciones sanguinolentas.</li>
                    </ul>
