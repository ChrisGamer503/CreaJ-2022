                <h1>Vómito</h1>
                <div class="content">
                    <div class="content2">
                        <div class="sepa">
                            <p>Los vómitos pequeños ocurren cuando el contenido del estómago del bebé vuelve a subir al
                                esófago después de una comida. Con los bebés, cuando el esfínter esofágico inferior se
                                abre.
                            </p>
                            <p>También puede ocurrir cuando los bebés tosen, lloran o se ponen tensos, El
                                vómito pequeño es muy común en bebés sanos. De hecho, más de la mitad de todos los bebés
                                vomitan en los primeros 3 meses de vida..
                            </p>
                        </div>
