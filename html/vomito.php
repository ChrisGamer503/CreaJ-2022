                <h1>Vomiting</h1>
                <div class="content">
                    <div class="content2">
                        <div class="sepa">
                            <p>Small vomiting occurs when the contents of the baby's stomach rise back up into the
                                esophagus after a meal. With babies, when the lower esophageal sphincter
                                opens.
                            </p>
                            <p> It can also happen when babies cough, cry, or tense up.
                                Small vomiting is very common in healthy babies. In fact, more than half of all babies
                                vomit in the first 3 months of life.
                            </p>
                        </div>
                        <div class="sepa2">
                        <img src="../multimedia/imagenes/Imagenes-enfermedades/bebes/vomito.png">
                        </div>
                    </div>
                </div>
                <div class="sintom">
                    <p>Causes</p>
                </div>
                <div class="info-causa">
                    <ul>
                        <li>Food allergies.</li>
                        <li>Migraines.</li>
                        <li>Infections of the stomach or intestines, such as "epidemic viral gastroenteritis" or
                            food poisoning.</li>
                        <li>Drugs or treatments, such as chemotherapy or radiation therapy for cancer</li>
                        <li>Returning of stomach contents (food or liquid) (also called reflux
                            gastroesophageal or GERD).</li>
                        <li>Morning sickness during pregnancy.</li>
                        <li>Dizziness or motion sickness.</li>
                        <li>Severe pain, such as from kidney stones.</li>
                    </ul>
                </div>
                <div class="sintoma">
                    <p>Symptom</p>
                </div>
                <div class="lista-sin">
                    <ul>
                        <li>Lack of appetite</li>
                        <li>Dehydration after vomiting.</li>
                        <li>Vertigo.</li>
                        <li> Lethargy or apathy.</li>
                        <li>In infants, fussiness or irritability and bulging of the soft spots between the
                            skull bones (fontanelles).</li>
                        <li>In older children, severe headache, neck stiffness that makes it difficult to lower the
                            chin to chest, sensitivity to light, and fever.</li>
                        <li>Abdominal pain and/or swelling.</li>
                        <li>Persistent vomiting in children who are not growing or developing as expected.</li>
                        <li>Bloody stools.</li>
                    </ul>
                </div>
                <div class="tratamientos">
                    <p>Treatment</p>
                </div>
                <div class="info-tratamientos">
                    <p>It is important to make sure that the child is well hydrated. fluids are given
                        usually by mouth (see Dehydration in Children). Solutions are used
                        oral rehydration tablets containing the proper balance of electrolytes. In United States,
                        These solutions are widely available over the counter at most pharmacies and
                        supermarkets. Sports drinks, carbonated drinks, juices and similar beverages have
                        too little sodium and excess carbohydrates and should not be used.</p>
                </div>
            </div>
