                    <p>Es importante asegurarse de que el niño está bien hidratado. Los líquidos se administran
                        habitualmente por vía oral (véase Deshidratación en niños). Se utilizan soluciones de
                        rehidratación oral que contienen el equilibrio adecuado de electrólitos. En Estados Unidos,
                        estas soluciones están ampliamente disponibles sin receta en la mayoría de las farmacias y los
                        supermercados. Las bebidas para deportistas, bebidas gaseosas, zumos y bebidas similares tienen
                        muy poco sodio y exceso de hidratos de carbono y no se deben usar.</p>
