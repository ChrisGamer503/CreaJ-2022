                        <li>Treatment with antibiotic:</li>
                        <li>Children 6 months and older with moderate to severe ear pain in one or both ears for at
                            least 48 hours or a temperature of 102.2 F (39 C) or higher</li>
                        <li>Children 6 to 23 months with mild middle ear pain in one or both ears for less than 48 hours
                            and a temperature less than 102.2 F (39 C)</li>
                        <li>Children 24 months and older with mild middle ear pain in one or both ears for less than 48
