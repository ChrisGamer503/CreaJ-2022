                        <li>Ear pain, especially when lying down</li>
                        <li>Pulling or pulling on an ear</li>
                        <li>Sleep disorders</li>
                        <li>Crying more than usual</li>
                        <li>Concern</li>
                        <li>Difficulty hearing or responding to sounds</li>
                        <li>Loss of balance</li>
                        <li>Fever</li>
                        <li>Discharge of fluid from the ear</li>
                        <li>Headache</li>
                        <li>Loss of appetite</li>
                    </ul>
